/********Color display description
      white  gray1  gray2  black
0x10|  01     01     00     00
0x13|  01     00     01     00
                                   ****************/
void pic_display_4bit (void)
{
  uint32_t i,j;
	uint8_t temp1,temp2,temp3;

    ESP_LOGE(TAG, "e-Paper pic_display_4bit");
	  //old  data
		send_command(0x10);

		for(i=0;i<4736;i++)	               //4736*4  296*128
		{
			temp3=0;
      for(j=0;j<4;j++)
			{
				temp1 = gImage_1[i*4+j];
				temp2 = temp1&0xF0 ;
				if(temp2 == 0xF0)
					temp3 |= 0x01;//white
				else if(temp2 == 0x00)
					temp3 |= 0x00;  //black
				else if((temp2>0xA0)&&(temp2<0xF0))
					temp3 |= 0x01;  //gray1
				else
					temp3 |= 0x00; //gray2
				temp3 <<= 1;
				temp1 <<= 4;
				temp2 = temp1&0xF0 ;
				if(temp2 == 0xF0)  //white
					temp3 |= 0x01;
				else if(temp2 == 0x00) //black
					temp3 |= 0x00;
				else if((temp2>0xA0)&&(temp2<0xF0))
					temp3 |= 0x01; //gray1
				else
						temp3 |= 0x00;	//gray2
        if(j!=3)
			  temp3 <<= 1;


		 }
       	send_data(temp3);
		}
    //new  data
		send_command(0x13);

		for(i=0;i<4736;i++)	               //4736*4   296*128
		{
			temp3=0;
      for(j=0;j<4;j++)
			{
				temp1 = gImage_1[i*4+j];
				temp2 = temp1&0xF0 ;
				if(temp2 == 0xF0)
					temp3 |= 0x01;//white
				else if(temp2 == 0x00)
					temp3 |= 0x00;  //black
				else if((temp2>0xA0)&&(temp2<0xF0))
					temp3 |= 0x00;  //gray1
				else
					temp3 |= 0x01; //gray2
				temp3 <<= 1;
				temp1 <<= 4;
				temp2 = temp1&0xF0 ;
				if(temp2 == 0xF0)  //white
					temp3 |= 0x01;
				else if(temp2 == 0x00) //black
					temp3 |= 0x00;
				else if((temp2>0xA0)&&(temp2<0xF0))
					temp3 |= 0x00;//gray1
				else
						temp3 |= 0x01;	//gray2
        if(j!=3)
			  temp3 <<= 1;

		 }
       	send_data(temp3);
		}
}


//LUT download
void lut(void)
{
	unsigned int count;
	{
		send_command(0x20);							//vcom
		for(count=0;count<44;count++)
			{send_data(lut_vcom[count]);}

	send_command(0x21);							//red not use
	for(count=0;count<42;count++)
		{send_data(lut_ww[count]);}

		send_command(0x22);							//bw r
		for(count=0;count<42;count++)
			{send_data(lut_bw[count]);}

		send_command(0x23);							//wb w
		for(count=0;count<42;count++)
			{send_data(lut_wb[count]);}

		send_command(0x24);							//bb b
		for(count=0;count<42;count++)
			{send_data(lut_bb[count]);}

		send_command(0x25);							//vcom
		for(count=0;count<42;count++)
			{send_data(lut_ww[count]);}
	}
}

void full_display(void pic_display(void))
{

		pic_display(); //picture
		lut(); //Power settings
		send_command(0x12);			//DISPLAY REFRESH
		delay_ms(100);	    //!!!The delay here is necessary, 200uS at least!!!
		wait_untile_idle();
}

/////////////////////////////Enter deep sleep mode////////////////////////
void EPD_sleep(void) //Enter deep sleep mode
{
		send_command(0X50);
		send_data(0xf7);
		send_command(0X02);  	//power off
	  wait_untile_idle();
		send_command(0X07);  	//deep sleep
		send_data(0xA5);
}


int epd3in9_init(void)
{
    reset_pin = RST_PIN;
    dc_pin = DC_PIN;
    cs_pin = CS_PIN;
    busy_pin = BUSY_PIN;
    width = EPD_WIDTH;
    height = EPD_HEIGHT;

    /* this calls the peripheral hardware interface, see epdif */
    if (ifinit() != 0) {
        return -1;
    }
    /* EPD hardware init start */
    reset();

    ESP_LOGE(TAG, "e-Paper init");
    send_command(0x01);			//POWER SETTING
    send_data(0x03);
    send_data(0x00);
    send_data(0x2b);
    send_data(0x2b);
    send_data(0x13);

    send_command(0x06);         //booster soft start
    send_data(0x17);		//A
    send_data(0x17);		//B
    send_data(0x17);		//C

    send_command(0x04);
    wait_untile_idle();

    send_command(0x00);			//panel setting
    send_data(0x3f);		//KW-3f   KWR-2F	BWROTP 0f	BWOTP 1f

    send_command(0x30);			//PLL setting
    send_data(0x3c);      	//100hz

    send_command(0x61);			//resolution setting
    send_data(0x80);
    send_data(0x01);
    send_data(0x28);

    send_command(0x82);			//vcom_DC setting
    send_data(0x12);

    send_command(0X50);			//VCOM AND DATA INTERVAL SETTING
    send_data(0x97);
    /* EPD hardware init end */

    ESP_LOGE(TAG, "e-Paper init Ok");
    full_display(pic_display_4bit); //pic1
    ESP_LOGE(TAG, "e-Paper display");
	EPD_sleep(); //Enter deep sleep mode
    ESP_LOGE(TAG, "e-Paper sleep");
    return 0;
}

/**
 *  @brief: basic function for sending commands
 */
void send_command(unsigned char command)
{
    digital_write(dc_pin, 0);
    spi_transfer(command);
}

/**
 *  @brief: basic function for sending data
 */
void send_data(unsigned char data)
{
    digital_write(dc_pin, 1);
    spi_transfer(data);
}

/**
 *  @brief: Wait until the busy_pin goes HIGH
 */
void wait_untile_idle(void)
{
    int busy = 0;
    do {
        send_command(GET_STATUS);
        busy = digital_read(busy_pin);
        busy = !(busy & 0x01);
        delay_ms(100);
    }
    while(busy);
}

/**
 *  @brief: module reset.
 *          often used to awaken the module in deep sleep,
 *          see sleep();
 */
void reset(void)
{
    digital_write(reset_pin, 0);
    delay_ms(200);
    digital_write(reset_pin, 1);
    delay_ms(200);
}

/**
 * @brief Transmit partial data to the SRAM
 *
 * @param char
 * @param char
 * @param x
 * @param y
 * @param w
 * @param l
 */
void set_partial_window(const unsigned char* buffer_black, const unsigned char* buffer_red, int x, int y, int w, int l)
{
    send_command(PARTIAL_IN);
    send_command(PARTIAL_WINDOW);
    send_data(x >> 8);
    send_data(x & 0xf8); // x should be the multiple of 8, the last 3 bit will always be ignored
    send_data(((x & 0xf8) + w - 1) >> 8);
    send_data(((x & 0xf8) + w - 1) | 0x07);
    send_data(y >> 8);
    send_data(y & 0xff);
    send_data((y + l - 1) >> 8);
    send_data((y + l - 1) & 0xff);
    send_data(0x01); // Gates scan both inside and outside of the partial window. (default)
    delay_ms(2);
    send_command(DATA_START_TRANSMISSION_1);
    if (buffer_black != NULL) {
        for (int i = 0; i < w / 8 * l; i++) {
            send_data(buffer_black[i]);
        }
    }
    delay_ms(2);
    send_command(DATA_START_TRANSMISSION_2);
    if (buffer_red != NULL) {
        for (int i = 0; i < w / 8 * l; i++) {
            send_data(buffer_red[i]);
        }
    }
    delay_ms(2);
    send_command(PARTIAL_OUT);
}

